        </p>
        <p>
          Most agent frameworks live in the cloud. TARS lives on your machine, owns its own memory, and stays out of your way. It opens a browser console for chat, but also runs background jobs, watches itself, and reflects nightly to extract durable experience from sessions.
        </p>
        <p>
          Extension is intentionally lean: skills (Markdown + companion CLI) load only when invoked — not every chat turn. Plugins and MCP servers are gated, so the system prompt stays small and the agent stays focused.
